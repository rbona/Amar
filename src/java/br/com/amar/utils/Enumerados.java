package br.com.amar.utils;

/**
 *
 * @author Rodrigo
 */
public class Enumerados {
    public enum ModoGrade{
        /**
         * Indica que a grade está listando os dados
         */
        LISTAGEM(0), 
        /**
         * Indica que a grade está incluindo um registro
         */
        INCLUSAO(1), 
        /**
         * Indica que a grade está alterando um registro
         */
        ALTERACAO(2);
        
        /**
         * Armazena o valor de um enumerado
         */
        private final int valor;
        
        ModoGrade(int valorEnumerado){
            valor = valorEnumerado;
        }
        
        /**
         * Retorna o valor do enumerado
         * @return inteiro que representa o enumerado
         */
        public int getValor(){return this.valor;}
    }
}
